package com.qf.a_test;

public class Demo7 {
	public static void main(String[] args) {
		//打印一个字符类型的'
		System.out.println('\t');
		//打印一个字符类型\
		System.out.println('\\');
		
		//字符串类型的数据
		System.out.println("hello world");
		//打印一个字符串类的 "
		System.out.println("\"");
		//打印一个字符串的""
		System.out.println("\"\"");
		//打印一个字符串  \\
		System.out.println("\\\\");
		//\t \n 只能在字符和字符串中体现效果~！！！
		System.out.println("说的那家吗\t南山");
		System.out.println("孤舟蓑笠翁\n独钓寒江雪");
		
	}
}
